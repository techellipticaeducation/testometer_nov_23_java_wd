package testometer_nov_23_java_wd;

public class ComparisonOperator {
	public static void main(String[] args) {
		// return = boolean
		// == != > >= < <=
		
		int i = 10;
		int j = 10;
		System.out.println(i == j); // true
		System.out.println(i != j); // false 
		System.out.println(i > j); // false
		System.out.println(i >= j); // true
		System.out.println(i < j); // false
		System.out.println(i <= j); // true
	}
}
